package im.mrx.leolanguage.leo.formatting

import com.intellij.lang.BracePair
import com.intellij.lang.PairedBraceMatcher
import com.intellij.psi.PsiFile
import com.intellij.psi.tree.IElementType
import im.mrx.leolanguage.leo.psi.LeoTypes

class LeoPairedBraceMatcher : PairedBraceMatcher {
    override fun getPairs(): Array<BracePair> {
        return arrayOf(PAIR)
    }

    override fun isPairedBracesAllowedBeforeType(lbraceType: IElementType, contextType: IElementType?): Boolean {
        return true
    }

    override fun getCodeConstructStart(file: PsiFile?, openingBraceOffset: Int): Int {
        // TODO: go to correct position, see javadoc
        return openingBraceOffset
    }

    companion object {
        val PAIR = BracePair(LeoTypes.LBRACE, LeoTypes.RBRACE, true)
    }
}